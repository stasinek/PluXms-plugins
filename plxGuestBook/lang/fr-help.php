<h2>Installation</h2>
<p><b>Pensez &agrave; activer le plugin.</b><br/>
<p>&nbsp;</p>
<h2>Utilisation</h2>
<p>
	Le plugin ajoute une entrée "Livre d'or" dans la barre des menus à gauche, dans l&#039;administration du site.<br>
	Vous pouvez administrer vos messages à partir de cette page.	
</p>
<p>&nbsp;</p>
<h2>Configuration</h2>
<p>
	Dans la configuration du plugin (Paramètres > plugins > Livre d'or > configuration), vous pouvez:
	<ul>
		<li>Configurer l&#039;utilisation du plugin pour la partie publique.</li>
	</ul>
</p>
